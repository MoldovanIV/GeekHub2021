#Написати функцію < square >, яка прийматиме один аргумент - сторону квадрата,
#і вертатиме 3 значення (кортеж): периметр квадрата, площа квадрата та його діагональ.
import math
def square(a):
    p = 4*a
    s = a*a
    d = a*math.sqrt(2)
    rez=(p,s,d)
    return rez
st=float(input("Сторона квадрата = "))
print("P, S, d квадрата відповідно дорівнюють:\n",square(st))
    
